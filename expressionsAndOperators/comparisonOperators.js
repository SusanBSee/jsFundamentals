/*Comparison Operators */

/*Equal to 
    - Java assumes we are trying to check if the string '3' is equal to the number 3
    - Will use 2 ==*/

'3' == 3;
console.log('3' == 3);

/*Strict equal to
    - Compares the datatype and value of the two values to check if they are equal
    - will use 3 ===*/

console.log('3' === 3 );

/*Not equal to*/
console.log('3' !=3);

/*Strict not equal to
  */
 console.log('3' !==3);

 /*Greater than */
 console.log(3 > 23);

 /*Less than */
 console.log(2 < 3);

 /*Greater than or equal to */
 console.log(3 >= 3);

 /*Less than or equal to*/
 console.log(2 <=3);

 /*'And'operator - Must use two &&*/
 2 && 3;

 /*'Or" operator -  */
 2 || 3;
